void faceSetup() {
  // initialize the SSD1306
  if (!face.begin(SSD1306_SWITCHCAPVCC, FACE_ADDRESS)) {
    for (;;)
      ;
  }

  // SSD1306_SWITCHCAPVCC = generate display voltage from 3.3V internally
  if (!display.begin(SSD1306_SWITCHCAPVCC, MOUTH_ADDRESS)) {
    for (;;)
      ;  // Don't proceed, loop forever
  }

  display.setRotation(2);
  display.clearDisplay();
  face.clearDisplay();
}

void showFace(const unsigned char* image) {
  face.clearDisplay();
  face.drawBitmap((FACE_WIDTH - 128) / 2, (FACE_HEIGHT - 64) / 2, image, 128, 64, 1);
  face.display();
}
