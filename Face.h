// Mouth displays on a single line centered
void report(String news, String topic) {
  // Print on the mouth
  sayTopic(topic, 2);
  // print on Face character by character, line by line.
  showNews(news);
}

void showNews(String news) {
  // print on Face character by character, line by line.
  face.clearDisplay();
  face.setCursor(0, 1);
  face.setTextSize(1);       // Set text size to original size
  face.setTextColor(WHITE);  // Draw white text

  int textLength = news.length();
  int lineLength = 0;

  for (int i = 0; i < textLength; i++) {
    if (lineLength >= FACE_WIDTH / 6) {  // 6 pixels per character
      face.println();
      lineLength = 0;
    }

    face.write(news[i]);
    face.display();
    delay(10);
    lineLength++;
  }
}
void showNews(int news) {
  showNews(String(news));
}
void showNews(float news) {
  showNews(String(news));
}

